    private void jButtonSelectImgMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonSelectImgMouseClicked
        // TODO add your handling code here:
        JFileChooser jfc = new JFileChooser("./Imagenes");
        int opc = jfc.showOpenDialog(null);
        if (opc== jfc.APPROVE_OPTION) {
            File f = jfc.getSelectedFile();
            this.rutaImagen = f.getPath();
            
            this.rutaImagen = this.rutaImagen.substring(this.rutaImagen.indexOf("Imagenes"));
            ImageIcon img = new ImageIcon(rutaImagen);
            jLabelImagen.setText("");
            jLabelImagen.setIcon(img);
        }
    }//GEN-LAST:event_jButtonSelectImgMouseClicked

    private void jButtonAddUsuMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonAddUsuMouseClicked
        // TODO add your handling code here:
        String nombre = jTextFieldNombre.getText();
        String pass = jTextFieldContraseña.getText();
        String equipo = (String) jComboBoxEquipos.getSelectedItem();
        int edad = (int) jSpinnerEdad.getValue();
        String imagen = rutaImagen;
        
        if ((!nombre.equalsIgnoreCase("nombre") || !nombre.equals("") ) && !pass.equalsIgnoreCase("contraseña") && edad>18 && !rutaImagen.equals("")) {
            Usuario u = new Usuario(nombre,pass,equipo,edad,imagen);
            this.vUsuario.add(u);
            Controlador.IODatos.addusers(u);
            jTextFieldContraseña.setText("");
        jTextFieldNombre.setText("");
        jSpinnerEdad.setValue(0);
        jLabelImagen.setText("Añadir Imagen");
        jLabelImagen.setIcon(null);
        }
        else{
                JOptionPane.showMessageDialog(rootPane, "se ha introducido mal algun dato, revisalo y vuelve a intentar");
                }
    }//GEN-LAST:event_jButtonAddUsuMouseClicked

