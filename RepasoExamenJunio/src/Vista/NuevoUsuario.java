    private void jTextFieldContraseñaFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldContraseñaFocusLost
        // TODO add your handling code here:
        String passwd="";
        passwd=jTextFieldContraseña.getText();
        Pattern pat = Pattern.compile("[a-z A-Z 0-9]{6,10}");
        Matcher mat = pat.matcher(passwd);
        if(mat.matches()){
            JOptionPane.showMessageDialog(rootPane, "La contraseña tiene el formato correcto");
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "El formato de la contraseña es incorrecto, debe tener un minimo de 6 caracteres y un maximo de 10");
        }
        
    }//GEN-LAST:event_jTextFieldContraseñaFocusLost

    private void jTextFieldNombreFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldNombreFocusLost
        // TODO add your handling code here:
        String nombre="";
        nombre=jTextFieldNombre.getText();
        boolean existe = Controlador.IODatos.comprobaruserexists(nombre);
        if (existe) {
            jTextFieldNombre.setText("");
            JOptionPane.showMessageDialog(rootPane, "Este usuario ya existe, utilize otro");
            
            
        }
    }//GEN-LAST:event_jTextFieldNombreFocusLost

